public class buku {
    public String judul;
    public String pengarang;
    public String penerbit;
    public int tahun ;
    public int kategori;
    public String keterangan;

    public buku(String judul, String pengarang, String penerbit, int tahun, int kategori, String keterangan){
        this.judul = judul;
        this.pengarang = pengarang;
        this.penerbit = penerbit;
        this.tahun = tahun;
        this.kategori = kategori;
        this.keterangan = keterangan;
    }
}